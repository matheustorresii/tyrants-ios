import SwiftUI

extension Font {
    static func pressStart(size: CGFloat) -> Font {
        return .custom("PressStart2P-Regular", size: size)
    }
    
    static func sixtyfour(size: CGFloat) -> Font {
        return .custom("Sixtyfour", size: size)
    }
    
    static func tiny5(size: CGFloat) -> Font {
        return .custom("Tiny5", size: size)
    }
}
